// sortedColorPhrases is sorted by length (longest first) for matching.
var sortedColorPhrases []string

func init() {
	for phrase := range colorWordMap {
		sortedColorPhrases = append(sortedColorPhrases, phrase)
	}
	sort.Slice(sortedColorPhrases, func(i, j int) bool {
		return len(sortedColorPhrases[i]) > len(sortedColorPhrases[j])
	})
}
